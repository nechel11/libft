#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	int		i;
	int		dlina;

	i = 0;
	dlina = ft_strlen(s1);
	str = (char *)malloc(sizeof(char) * dlina + 1);
	if (!str)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
